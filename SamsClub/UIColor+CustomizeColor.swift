import UIKit

extension UIColor {
    open class var scBlue: UIColor {
        get {
            return UIColor(red: 31.0/255, green: 90.0/255, blue: 152.0/255, alpha: 1.0)
        }
    }
    open class var scBackgroundGray: UIColor {
        get {
            return UIColor(red: 0xf1/255, green: 0xf1/255, blue: 0xf1/255, alpha: 1.0)
        }
    }
}
